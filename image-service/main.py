import os
from contextlib import asynccontextmanager
import uvicorn
from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, field_validator


@asynccontextmanager
async def lifespan(app: FastAPI):
    log.info("image-service ready  model=%s  pipeline=%s",
             MODEL_ID, "loaded" if _model else "heuristic-only")
    yield


app = FastAPI(title="Image Detection Service", version="1.0.0", lifespan=lifespan)
app.add_middleware(CORSMiddleware,
    allow_origins=["*"], allow_credentials=True,
    allow_methods=["*"], allow_headers=["*"])


class ImageRequest(BaseModel):
    image: str

    @field_validator("image")
    @classmethod
    def _check(cls, v: str) -> str:
        if not v or len(v.strip()) < 20:
            raise ValueError("image must be a non-empty base64 string")
        return v.strip()


@app.get("/health")
async def health():
    return {
        "status":   "ok",
        "service":  "image-detection",
        "model":    MODEL_ID,
        "pipeline": _model is not None,
    }


@app.post("/detect")
async def detect(req: ImageRequest):
    result = await analyze_image(req.image)
    return result   # analyze_image already returns full structured response


